// ─── Markdown builder ─────────────────────────────────────────────────────────

function scopeToMarkdown(text: string): string {
  // Convert ## ZONE NAME ## delimiters to #### sub-headings for .md export
  return text.replace(/##\s*([^#\n]+?)\s*##\n?/g, '\n#### $1\n');
}

function buildMarkdown(
  data: ProjectData,
  contractor: ContractorProfile,
  refNumber: string,
  generatedDate: string,
  refinedData: RefinedBriefData | null,
): string {
  const lines: string[] = [];

  lines.push('# ScopeLock — Project Brief');
  lines.push(`**Reference:** ${refNumber}  `);
  lines.push(`**Date:** ${generatedDate}`);
  lines.push('');

  if (contractor.companyName) {
    lines.push(`**Prepared by:** ${contractor.companyName}`);
    if (contractor.contactName) lines.push(`**Contact:** ${contractor.contactName}`);
    if (contractor.phone)       lines.push(`**Phone:** ${contractor.phone}`);
    if (contractor.email)       lines.push(`**Email:** ${contractor.email}`);
    if (contractor.licenseNumber) lines.push(`**Licence:** ${contractor.licenseNumber}`);
    if (contractor.abn)         lines.push(`**ABN:** ${contractor.abn}`);
    lines.push('');
  }

  if (refinedData?.projectNarrative) {
    lines.push('## Project Narrative');
    lines.push(refinedData.projectNarrative);
    lines.push('');
  }

  lines.push('## Client & Project Details');
  lines.push(`**Client:** ${data.clientName || '—'}`);
  lines.push(`**Site Address:** ${data.siteAddress || '—'}`);
  lines.push(`**Budget Range:** ${data.budgetRange || '—'}`);
  lines.push('');

  if (data.primaryMotivation.length > 0) {
    lines.push('## Primary Motivations');
    data.primaryMotivation.forEach((m) => lines.push(`- ${m}`));
    if (refinedData?.motivationStatement) {
      lines.push('');
      lines.push(`> ${refinedData.motivationStatement}`);
    }
    lines.push('');
  }

  lines.push('## Design Vision');
  if (data.architecturalStyles.length > 0) {
    lines.push(`**Architectural Style:** ${data.architecturalStyles.join(', ')}`);
    if (refinedData?.designPhilosophy) {
      lines.push('');
      lines.push(refinedData.designPhilosophy);
    }
    lines.push('');
  }
  if (data.lifestyleGoals.length > 0) {
    lines.push('**Lifestyle Goals:**');
    data.lifestyleGoals.forEach((g) => lines.push(`- ${g}`));
    if ((refinedData?.lifestyleScopeItems?.length ?? 0) > 0) {
      lines.push('');
      (refinedData!.lifestyleScopeItems ?? []).forEach((item) => lines.push(`- ${item}`));
    }
    lines.push('');
  }

  lines.push('## Room Specifications');
  lines.push('');
  lines.push('### Kitchen');
  lines.push(scopeToMarkdown(refinedData?.kitchenScope || data.kitchenNotes || '_No notes entered._'));
  lines.push('');
  lines.push('### Master Bedroom Suite');
  lines.push(scopeToMarkdown(refinedData?.masterBedroomScope || data.masterBedroomNotes || '_No notes entered._'));
  lines.push('');
  lines.push('### Living Zones');
  lines.push(scopeToMarkdown(refinedData?.livingZoneScope || data.livingZoneNotes || '_No notes entered._'));
  lines.push('');

  const additionalText = refinedData?.additionalScope || data.additionalNotes;
  if (additionalText) {
    lines.push('## Additional Requirements');
    lines.push(scopeToMarkdown(additionalText));
    lines.push('');
  }

  lines.push('---');
  lines.push(`_Generated by ScopeLock · ${generatedDate} · Ref ${refNumber}_`);
  lines.push('_Preliminary brief — subject to design development and formal quotation._');

  return lines.join('\n');
}

